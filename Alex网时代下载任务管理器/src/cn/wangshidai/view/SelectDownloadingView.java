	// 开启一条子线程去刷新表格
	private void refreshThread() {
		Thread thread = new Thread(new Runnable() {
			@Override
			public void run() {
				while (isRefresh) {
					loadTable();

					try {
						Thread.sleep(1000);
					} catch (InterruptedException e) {
						// TODO Auto-generated catch block
						e.printStackTrace();
					}
				}
			}
		});
		thread.setDaemon(true);
		thread.start();
	}

	private void loadTable() {
		DefaultTableModel tableModel = (DefaultTableModel) this.jTable1
				.getModel();

		while (tableModel.getRowCount() > 0) {
			tableModel.removeRow(0);
		}

		// 序号 文件名 日期 大小 状态
		for (int i = 0; i < PublicUtil.wangshidaiList.size(); i++) {
			TaskDownloadInfo info = PublicUtil.wangshidaiList.get(i);

			String statusStr = info.isIs_ok() ? "已完成" : "下载中";
			if (info.isIs_pause()) {
				String paswString = "暂停中";
				Object[] array = { i + 1, info.getFile_name(), info.getFile_date(),
						info.getFile_size(), paswString };
				tableModel.addRow(array);
			}else {
				Object[] array = { i + 1, info.getFile_name(), info.getFile_date(),
						info.getFile_size(), statusStr };
				tableModel.addRow(array);
			}

			
		}

	}

