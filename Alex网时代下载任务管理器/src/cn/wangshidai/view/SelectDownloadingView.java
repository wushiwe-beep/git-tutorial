	private void deleteClick(java.awt.event.ActionEvent evt) {	
		if(this.tableSelectedIndex==-1){
			JOptionPane.showMessageDialog(null, "请先选择要删除的任务");
		}else{
			int result = JOptionPane.showConfirmDialog(null, "是否需要删除本地文件？");
			System.out.println("result is "+result);
			if(result==0){
				//删除目录中的指定文件
				TaskDownloadInfo info = PublicUtil.wangshidaiList.get(this.tableSelectedIndex);
				File file = new File(PublicUtil.MY_HOME, info.getFile_name());
				file.delete();
				info.setIs_delete(true);
			}

			//从集合中删除指定下标的元素
			PublicUtil.wangshidaiList.remove(this.tableSelectedIndex);
			
			
			JOptionPane.showMessageDialog(null, "删除成功！");

			this.jTextField1.setText("");
			this.jTextField2.setText("");
			this.jTextField3.setText("");
			this.jTextField4.setText("");
			
			
		}
		
		
	}

	private void pauseClick(java.awt.event.ActionEvent evt) {
		if(this.tableSelectedIndex==-1){
			JOptionPane.showMessageDialog(null, "请先选择要暂停的任务");
		}else{
			TaskDownloadInfo info = PublicUtil.wangshidaiList.get(this.tableSelectedIndex);
			
			if(this.jButton1.getText().equals("暂停")){//暂停按钮
				info.setIs_pause(true);
				this.jButton1.setText("继续");
				
			}else{//继续按钮
				info.setIs_pause(false);
				info.interrupt();//叫醒它！！！
				this.jButton1.setText("暂停");
			}
			
			
		}
		
	}

	private void jTable1MousePressed(java.awt.event.MouseEvent evt) {
		// 暂停刷新table
		this.isRefresh = false;

		// 获取表格的选择行
		int selectRow = this.jTable1.getSelectedRow();
		this.tableSelectedIndex = selectRow;
		
		// 从集合中取出对应行的TaskDownloadInfo对象
		TaskDownloadInfo info = PublicUtil.wangshidaiList.get(selectRow);

		this.jTextField1.setText(info.getFile_name());
		this.jTextField2.setText(info.getFile_size() + "b");

		String statusStr = info.isIs_ok() ? "已完成" : "下载中";
		String paswString = "暂停中";
		if(info.isIs_pause()) {
			this.jTextField3.setText(paswString);
		}else {
			this.jTextField3.setText(statusStr);
		}

		this.jTextField4.setText(info.getFile_date());

		// 恢复刷新table
		this.isRefresh = true;
		if(info.isIs_pause()) {
			this.jButton1.setText("暂停");
		}else {
			this.jButton1.setText("继续");
		}
	}

